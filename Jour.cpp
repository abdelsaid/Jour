#include <string.h>
#include "Jour.h"


const char * const Jour::jours[] = {
	"dimanche",
	"lundi",
	"mardi",
	"mercredi",
	"jeudi",
	"vendredi",
	"samedi"
};

/**
* Construit le jour Dimanche
*/
// ----------------------------------------------------------------------------
Jour::Jour()
// ----------------------------------------------------------------------------
{
	numero = 0;
}

/**
* Construit un jour avec une valeur specifiee.
*/
// ----------------------------------------------------------------------------
Jour::Jour( int numero )
// ----------------------------------------------------------------------------
{
	this->numero = numero%7;
}

/**
* Construit un jour avec un jour en chaine de caractairevaleur specifiee.
*/
// ----------------------------------------------------------------------------
Jour::Jour( char *JourDeSemaine )
// ----------------------------------------------------------------------------
{
	for(int i=0;i<7;i++)
	{
		if(!strcmp(jours[i],JourDeSemaine)) numero = i;
	}
}

/**
* Operateur de conversion de type Jour en un type entier.
*/
// ----------------------------------------------------------------------------
Jour::operator int()const
// ----------------------------------------------------------------------------
{
	return numero%7;
}

// ----------------------------------------------------------------------------
Jour::operator ++(int)
// ----------------------------------------------------------------------------
{
	int tmp = this->numero;
	numero++;
	return tmp;
}

// ----------------------------------------------------------------------------
Jour::operator ++(void)
// ----------------------------------------------------------------------------
{
	numero++;
	return this->numero;
}

// ----------------------------------------------------------------------------
Jour::operator --(int)
// ----------------------------------------------------------------------------
{
	int tmp = numero;
	if(numero == 0) numero = 6;
	else numero--;
	return tmp;
}

// ----------------------------------------------------------------------------
Jour::operator --(void)
// ----------------------------------------------------------------------------
{
	if(numero == 0) numero = 6;
	else --numero;
	return numero;
}

/**
* affiche le continu d'un objet de type jour
*/
// ----------------------------------------------------------------------------
ostream & operator <<(ostream & sortie, const Jour & jour)
// ----------------------------------------------------------------------------
{
	return sortie << Jour::jours[jour.numero%7];
}





